'''
    tupla é uma estrutura imutável, já a lista é mutável
'''

frutas = (
    "laranja",
    "pera",
    "uva",
)   # coloca-se uma vírgula no final para identificar que é uma tupla
print(frutas)

letras = tuple("python")
print(letras)

numeros = tuple([1, 2, 3, 4])
print(numeros)

pais = ("Brasil",)
print(pais)
